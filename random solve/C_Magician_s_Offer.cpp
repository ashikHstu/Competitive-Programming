vector<ll> primes;
int chk[2000006];
int N = 2000006;
int md = 10000000;
void sieve()
{
    primes.pb(2);
    chk[1] = 1;
    int sq = sqrt(N);
    for (ll i = 3; i <= sq; i += 2)
    {
        if (chk[i] == 0)
        {

            for (ll j = i * i; j <= N; j += (i + i))
            {
                chk[j] = 1;
            }
        }
    }

    for (int i = 3; i <= N - 2; i += 2)
    {
        if (chk[i] == 0)
            primes.pb(i);
    }
}

int ND(ll d)
{
    int ct = 0;
    while (d > 0)
    {
        ct++;
        d /= 10;
    }
    return ct;
}
void zeroPrint(int ct)
{
    for (int i = 0; i < ct; i++)
        cout << "0";
}

void solve(int kase)
{
    ll a, b;
    cin >> a >> b;
    vector<ll> p;
    ll mainRes = 1;
    ll lv = a;
    for (ll i = 0; primes[i] * primes[i] <= lv; i++)
    {
        ll t = 0;
        while (a % primes[i] == 0)
        {
            a /= primes[i];
            t++;
        }
        mainRes *= ((t * b) + 1);
        mainRes %= md;
    }

    if (a != 1)
    {
        mainRes *= (1 + b);
        mainRes %= md;
    }
    mainRes = mainRes % md;
    // mainRes = 0;
    int nd = ND(mainRes);
    if (nd == 0)
        nd = 1;
    cout << "Case " << kase << ": ";
    zeroPrint(7 - nd);
    cout << mainRes << endl;
}

