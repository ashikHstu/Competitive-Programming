ll findCost(string st, ll cost)
{
    int n = st.size();
    ll total = 0;
    for (int i = 0; i < n / 2; i++)
    {
        if (st[i] == st[n - i - 1])
            continue;
        else
            total += cost;
    }
    return total;
}

void solve()
{
    ll n, a, b;
    cin >> n >> a >> b;
    string st;
    cin >> st;
    ll swapCost = 0;
    ll res = 1000000000000000000LL;
    for (int i = 0; i < n; i++)
    {
        string ns;
        for (int j = i; j < n; j++)
            ns.pb(st[j]);
        for (int j = 0; j < i; j++)
            ns.pb(st[j]);

        // cout << "new St : " << ns << endl;
        ll curRes = findCost(ns, b);
        // cout << "cur cost " << i << " : " << curRes + swapCost << endl;
        res = min(res, curRes + swapCost);
        swapCost += a;
    }
    cout << res << endl;
}

int main()
{
    int tc = 1;
    // cin >> tc;
    for (int i = 1; i <= tc; i++)
    {
        solve();
    }

    return 0;
}

